package ris58h.agentmango;

public class GameCode {
    Integer id;
    String promo_code;
    Integer summ_promo;
    Integer sale_code;
    Integer status;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getPromo_code() {
        return promo_code;
    }

    public void setPromo_code(String promo_code) {
        this.promo_code = promo_code;
    }

    public Integer getSumm_promo() {
        return summ_promo;
    }

    public void setSumm_promo(Integer summ_promo) {
        this.summ_promo = summ_promo;
    }

    public Integer getSale_code() {
        return sale_code;
    }

    public void setSale_code(Integer sale_code) {
        this.sale_code = sale_code;
    }

    public Integer getStatus() {
        return status;
    }

    public void setStatus(Integer status) {
        this.status = status;
    }
}
